import { requireEnv } from '@/lib/env';
import { makeId, nowIso } from '@/lib/id';
import type { BacktestResult, StrategySpec } from '@/lib/types';
import { gzipSync } from 'node:zlib';

const PLAYBOOK_BASE_URL = 'https://api.bitget.com';

export async function runPlaybookBacktest(runId: string, strategy: StrategySpec): Promise<BacktestResult> {
  const accessKey = requireEnv('PLAYBOOK_ACCESS_KEY');
  const draftId = await uploadTemporaryPlaybook(accessKey, strategy);
  const playbookRunId = await dispatchRun(accessKey, draftId);
  const completed = await pollRun(accessKey, playbookRunId);
  const metrics = completed.metrics_output ?? completed.signal_output?.[0]?.metrics ?? {};
  const report = completed.backtest_report ?? {};

  return {
    backtest_id: playbookRunId,
    run_id: runId,
    provider: 'bitget_playbook',
    period: report.period_start && report.period_end ? `${report.period_start} to ${report.period_end}` : 'playbook-run-period',
    pnl: Number(metrics.total_return_pct ?? metrics.pnl ?? 0),
    sharpe: Number(metrics.sharpe_ratio ?? metrics.sharpe ?? 0),
    win_rate: Number(metrics.win_rate ?? 0),
    max_drawdown: Number(metrics.max_drawdown_pct ?? metrics.max_drawdown ?? 0),
    trade_count: Number(metrics.total_trades ?? metrics.trade_count ?? 0),
    raw_summary_ref: `playbook:${draftId}:${playbookRunId}`,
    created_at: nowIso(),
  };
}

async function uploadTemporaryPlaybook(accessKey: string, strategy: StrategySpec) {
  const archive = buildPlaybookArchive(strategy);
  const form = new FormData();
  form.append('package', new Blob([archive], { type: 'application/gzip' }), `${strategy.strategy_id}.tar.gz`);

  const response = await fetch(`${PLAYBOOK_BASE_URL}/api/v1/playbook/upload`, {
    method: 'POST',
    headers: { 'ACCESS-KEY': accessKey },
    body: form,
  });

  if (!response.ok) throw new Error(`Playbook upload failed: ${response.status}`);
  const data = await response.json();
  const draftId = data.draft_id ?? data.version_id ?? data.playbook_id;
  if (!draftId) throw new Error('Playbook upload response missing draft_id');
  return String(draftId);
}

async function dispatchRun(accessKey: string, draftId: string) {
  const response = await fetch(`${PLAYBOOK_BASE_URL}/api/v1/playbook/run`, {
    method: 'POST',
    headers: {
      'content-type': 'application/json',
      'ACCESS-KEY': accessKey,
    },
    body: JSON.stringify({ version_id: draftId }),
  });

  if (!response.ok) throw new Error(`Playbook run dispatch failed: ${response.status}`);
  const data = await response.json();
  if (!data.run_id) throw new Error('Playbook dispatch response missing run_id');
  return String(data.run_id);
}

async function pollRun(accessKey: string, playbookRunId: string) {
  const url = `${PLAYBOOK_BASE_URL}/api/v1/playbook/run?run_id=${encodeURIComponent(playbookRunId)}`;
  for (let attempt = 0; attempt < 30; attempt += 1) {
    const response = await fetch(url, { headers: { 'ACCESS-KEY': accessKey } });
    if (!response.ok) throw new Error(`Playbook run poll failed: ${response.status}`);
    const data = await response.json();
    if (data.status === 'completed') return data;
    if (data.status === 'failed') throw new Error(data.failure_reason || 'Playbook run failed');
    await new Promise((resolve) => setTimeout(resolve, 2000));
  }
  throw new Error('Playbook run timed out after 60 seconds');
}

function buildPlaybookArchive(strategy: StrategySpec) {
  const symbol = sanitizeSymbol(strategy.structured_strategy.symbol);
  const packageName = `tradetrace-${strategy.strategy_id}`.toLowerCase().replace(/_/g, '-').slice(0, 63);
  const files = new Map<string, string>();
  files.set('manifest.yaml', manifestYaml(packageName, symbol, strategy));
  files.set('backtest.yaml', backtestYaml(symbol));
  files.set('src/main.py', mainPy());
  files.set('src/strategy.py', strategyPy());
  return gzipSync(createTar(files));
}

