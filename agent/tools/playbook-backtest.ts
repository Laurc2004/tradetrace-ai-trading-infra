function mainPy() {
  return `import math
from typing import Any
from getagent import backtest, data, runtime


def _sanitize(value: Any) -> Any:
    if isinstance(value, float) and not math.isfinite(value):
        return None
    return value


def run() -> None:
    cfg = runtime.manifest.get("strategy_config", {}) or {}
    symbols = cfg.get("trading_symbols") or ["BTCUSDT"]
    symbol = symbols[0]
    bars = data.crypto.futures.kline(symbol=symbol, interval="1h", limit=1000)
    replay_frame = backtest.prepare_frame(bars, datetime_index="date")
    if replay_frame.empty:
        runtime.emit_signal(action="watch", symbol=symbol, confidence=0.0, metrics={"rows": 0})
        return
    instrument_key = f"{symbol}.BINANCE"
    result = backtest.run(ohlcv_data={instrument_key: replay_frame}, spec=runtime.backtest_spec)
    summary = result.summary or {}
    metrics = {
        "total_return_pct": _sanitize(result.total_return_pct),
        "sharpe_ratio": _sanitize(result.sharpe_ratio),
        "max_drawdown_pct": _sanitize(result.max_drawdown_pct),
        "win_rate": _sanitize(result.win_rate),
        "total_trades": _sanitize(result.total_trades),
        "profit_factor": _sanitize(result.profit_factor),
        "starting_balance": summary.get("starting_balance"),
        "rows": len(replay_frame),
    }
    action = "long" if (result.total_return_pct or 0) > 0 else "watch"
    runtime.emit_signal(action=action, symbol=symbol, confidence=_sanitize(result.win_rate) or 0.0, metrics=metrics)


if __name__ == "__main__":
    run()
`;
}

